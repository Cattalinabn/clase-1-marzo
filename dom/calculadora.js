function sum() {
 let v_nro1 = parseFloat(document.getElementById("nr1").value)
 let v_nro2 = parseFloat(document.getElementById("nr2").value)
 let suma = v_nro1 + v_nro2
 document.getElementById("resultado").innerHTML = suma
}
function resta(){
let v_nro1 = parseFloat(document.getElementById("nr1").value)
let v_nro2 = parseFloat(document.getElementById("nr1").value)
let resta = v_nro1 - v_nro2
document.getElementById("resultado").innerHTML = resta
}



 
